package ssj.model;

public class Par {

	private int prvi;
	private int drugi;
	
	public Par(int prvi, int drugi) {
		this.prvi = prvi;
		this.drugi = drugi;
	}

	public int getPrvi() {
		return prvi;
	}

	public void setPrvi(int prvi) {
		this.prvi = prvi;
	}

	public int getDrugi() {
		return drugi;
	}

	public void setDrugi(int drugi) {
		this.drugi = drugi;
	}
	
}
